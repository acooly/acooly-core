	/**
	 * 解析身份证信息
	 * 
	 * @param cardNo
	 * @return
	 */
	public static IdCardInfo parseIdCard(String cardNo) {

		if (StringUtils.isBlank(cardNo)) {
			throw new RuntimeException("身份证号码不能为空");
		}
		int len = StringUtils.trimToEmpty(cardNo).length();
		if (len != 15 && len != 18) {
			throw new RuntimeException("身份证号码长度错误，只能是15或18位");
		}
		IdCardInfo info = new IdCardInfo();
		info.setCardNo(cardNo);
		info.setBirthplace(birthplaces.get(StringUtils.substring(cardNo, 0, 6)));
		if (len == 18) {
			info.setBirthday(StringUtils.substring(cardNo, 6, 14));
			info.setGender(Integer.parseInt(StringUtils.substring(cardNo, 14,
					17)) % 2 == 0 ? "F" : "M");
		} else {
			info.setBirthday(StringUtils.substring("19" + cardNo, 6, 12));
			info.setGender(Integer.parseInt(StringUtils.substring(cardNo, 12,
					14)) % 2 == 0 ? "F" : "M");
		}
		return info;
	}

	static public class IdCardInfo {

		private String cardNo;
		private String gender;
		private String birthday;
		private String birthplace;

		public String getCardNo() {
			return cardNo;
		}

		public void setCardNo(String cardNo) {
			this.cardNo = cardNo;
		}

		public String getGender() {
			return gender;
		}

		public void setGender(String gender) {
			this.gender = gender;
		}

		public String getBirthday() {
			return birthday;
		}

		public void setBirthday(String birthday) {
			this.birthday = birthday;
		}

		public String getBirthplace() {
			return birthplace;
		}

		public void setBirthplace(String birthplace) {
			this.birthplace = birthplace;
		}

		@Override
		public String toString() {
			return String
					.format("IdCardInfo: {cardNo:%s, gender:%s, birthday:%s, birthplace:%s}",
							cardNo, gender, birthday, birthplace);
		}

	}

